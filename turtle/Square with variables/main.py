# Write a program in turtle that uses variables to draw a square.

import turtle

my_turtle = turtle.Turtle()

length = 100
angle = 90

my_turtle.forward(length)
my_turtle.left(angle)
my_turtle.forward(length)
my_turtle.left(angle)
my_turtle.forward(length)
my_turtle.left(angle)
my_turtle.forward(length)
